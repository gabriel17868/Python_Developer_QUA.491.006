# lista
nomes = ["Fulano ", "Cicrano", "Beltrano", "João", "Maria, José"]

# ordena a lista
nomes.sort(reverse=True) # Inverte a ordem alfabetica, de Z a A.

# imprime a lista na tela
print(nomes[0])
print(nomes[3])
for nome in nomes:
    print(nome)

# imprime o número de itens da lista
print(len(nomes))

